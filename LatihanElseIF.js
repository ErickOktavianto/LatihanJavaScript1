 var angka = prompt('Masukkan angkanya yang nau dilihat ganjil atau genap = ');    
if(angka % 2 === 0){
    alert(angka + ' Adalah bilangan genap dan silahkan refresh halaman jika ingin melihat bilangan lain');
} else if (angka % 2 ===     1){
    alert(angka + ' Adalah bilangan ganjil dan silahkan refresh halaman jika ingin melihat bilangan lain');
} else{
    alert('Yang Anda Masukkan Bukan Angka');    
}

